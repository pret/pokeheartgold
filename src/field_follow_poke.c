int OverworldModelLookupFormCount(int species) {
    if (!(species > 0 && species <= NATIONAL_DEX_COUNT)) {
        GF_ASSERT(0);
        species = 0;
    } else {
        species--;
    }
    return sFormMaxLUT[species];
}

BOOL OverworldModelLookupHasFemaleForm(int species) {
    if (!(species > 0 && species <= NATIONAL_DEX_COUNT)) {
        GF_ASSERT(0);
        species = 0;
    } else {
        species--;
    }
    return sFemaleFlagLUT[species];
}
