void FieldSystemUnkSub108_AddMonMood(struct FieldSystemUnk108 *unk, s8 by) {
    s8 mood;

    if (unk->mon == NULL) {
        GF_ASSERT(0);
        return;
    }

    mood = GetMonData(unk->mon, MON_DATA_MOOD, NULL);
    if (mood + by > 127) {
        mood = 127;
    } else if (mood + by < -127) {
        mood = -127;
    } else {
        mood += by;
    }
    SetMonData(unk->mon, MON_DATA_MOOD, &mood);
}

